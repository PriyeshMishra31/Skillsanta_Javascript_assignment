for(var evod = 20; evod <= 30; evod++)
{
    if (evod === 0){
        document.write(evod + " " + "This is a Even Number","</br>");
    }
    else if(evod % 2 === 0){
        document.write(evod + " " + "This is a Even Number", "</br>");
    }
    else{
        document.write(evod + " " + "This is Odd Number", "</br>")
    }
}